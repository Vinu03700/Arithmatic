public class Arithmatic{

    public static void main(String[] args) {
        int num1 = 20;
        int num2 = 5;

        int mul = num1 * num2;
        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + mul);

        int div = num1 / num2;
        System.out.println("The difference between " + num1 + " and " + num2 + " is: " + div);
    }
}
